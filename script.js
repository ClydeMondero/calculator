//* Add Function
function add(num1, num2) {
  let sum = num1 + num2;

  return sum;
}

//* Subraction Function
function subtract(num1, num2) {
  let difference = num1 - num2;

  return difference;
}
//* Multiplication Function
function multiply(num1, num2) {
  let product = num1 * num2;

  return product;
}
//* Division Function
function divide(num1, num2) {
  let divide = num1 / num2;

  return divide;
}

